# 元字符
# .  匹配任意字符（除了\n）
# \w  字母、数字、下划线
# \W  非字母、数字、下划线
# \s  任意空白字符
# \S  任意非空白字符
# \d  任意十进制数

# 限定符
# ?     匹配前面的字符0次或1次
# +     匹配前面的字符1次或多次
# *     匹配前面的字符0次或多次
# {n}   匹配前面的字符n次
# {n,}  匹配前面的字符最少n次
# {n,m} 匹配前面的字符最少n次，最多m次

# 其他字符
# 区间字符[]    匹配[]中指定的字符
# 排除字符^     匹配不在[]中指定的字符
# 选择字符|     用于匹配|左右的任意字符
# 转义字符
# [\u4e00-\u9fa5]  匹配任意一个汉字
# 分组()        改变限定符的作用

